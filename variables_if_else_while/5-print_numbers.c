#include <stdio.h>
#include <time.h>
#include <stdlib.h>

/**
 * main - void
 *
 * Description: prints all single digit numbers 0-9
 *
 * @: no parameters
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int number = 48;

	while (number <= 56)
	{
		printf("%d");
		number++;
	}
	printf("\n");
	return (0);
}
